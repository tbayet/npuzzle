#include "libft.h"

int	ft_lstlen(t_list **lst)
{
	int	len;

	len = 0;
	if (!lst)
	{
		if (!(*lst))
			return (0);
	}
	while (*lst)
	{
		len++;
		(*lst) = (*lst)->next;
	}
	return (len);
}
