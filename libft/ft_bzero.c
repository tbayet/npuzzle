#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*temp;

	if (s)
	{
		temp = (unsigned char*)s;
		while (n > 0)
		{
			temp[n - 1] = 0;
			n--;
		}
	}
}
