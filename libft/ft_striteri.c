#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int cpt;

	if (s && f)
	{
		cpt = 0;
		while (s[cpt] != '\0')
		{
			f(cpt, &s[cpt]);
			cpt++;
		}
	}
}
