#include "libft.h"

char	*ft_strcut(char *str, int start)
{
	int	i;

	i = 0;
	while (str[start + i] != '\0')
	{
		str[i] = str[start + i];
		i++;
	}
	while (str[i] != '\0')
	{
		str[i] = '\0';
		i++;
	}
	return (str);
}
